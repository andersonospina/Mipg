use App\Controllers\ModuleController;

class Diagnostics extends ModuleController
{

    //[Mipg/Config/Routes]


    public function __construct()
    {
        parent::__construct();
        $this->prefix = 'mipg-diagnostics';
        $this->module = 'App\Modules\Mipg';
        $this->views = $this->module . '\Views';
        $this->viewer = $this->views . '\index';
        helper($this->module . '\Helpers\Mipg');
    }

    public function index()
    {
        $url = base_url('mipg/diagnostics/home/' . lpk());
        return (redirect()->to($url));
    }


    public function home(string $rnd)
    {
        $this->oid = $rnd;
        $this->prefix = "{$this->prefix}-home";
        $this->component = $this->views . '\Diagnostics\Home';
        return (view($this->viewer, $this->get_Array()));
